//While

/* syntax: 
while condition {
    statement
} */


var i = 1

while i <= 10 { 
    print (i) 
    i = i + 1 
}
    
//--------------------------------------------------------------------------
//repeat
/*
syntax:
repeat {
	statement
}while condition

*/

var j = 1

repeat {
	print (j)
	j = j + 1
} while j < 10

//---------------------------------------------------------------------------
//for loop
/*
syntax :
for value in range {
	statement
} */

print ("Printing inside for loop")
for data in 1...10{
	print (data)
}




